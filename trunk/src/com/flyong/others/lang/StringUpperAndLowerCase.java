package com.flyong.others.lang;

/**
 * 字符串转化为大写或者小写字母。
 * 
 *
 * 
 */
public class StringUpperAndLowerCase {

  public static void main(String[] args) {
    String string = "abcdEFGH";
    // 转化为大写字母
    System.out.println(string.toUpperCase());

    // 转化为小写字母
    System.out.println(string.toLowerCase());
  }
}
